    private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
        ClienteService service = new ClienteService();
        ClientesDto cli = new ClientesDto(Integer.parseInt(txtID.getText()), txtNombre.getText(),
                txtDireccion.getText(), txtTelefono.getText());
            service.actualizarDatos(cli);
            tblModel.setRowCount(0);
            cargarDatos();
            limpiar();
    }//GEN-LAST:event_btnProcesarActionPerformed

    private void tblDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDatosMouseClicked
        txtID.setText(tblDatos.getValueAt(tblDatos.getSelectedRow(), 0).toString());
        txtNombre.setText(tblDatos.getValueAt(tblDatos.getSelectedRow(), 1).toString());
        txtDireccion.setText(tblDatos.getValueAt(tblDatos.getSelectedRow(), 2).toString());
        txtTelefono.setText(tblDatos.getValueAt(tblDatos.getSelectedRow(), 3).toString());
        btnProcesar.setEnabled(true);
    }//GEN-LAST:event_tblDatosMouseClicked

