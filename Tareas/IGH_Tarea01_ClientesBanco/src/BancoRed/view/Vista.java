    private void formatoTabla() {
        tblModel = new DefaultTableModel();
        tblModel.addColumn("ID");
        tblModel.addColumn("Nombre");
        tblModel.addColumn("Dirección");
        tblModel.addColumn("Telefono");
        this.tblDatos.setModel(tblModel);
    }

    private void cargarDatos() {
        ClienteService clienteService = new ClienteService();
        List<Object[]> datosClientes = clienteService.obtenerDatosClientes();
        for (Object[] fila : datosClientes) {
            tblModel.addRow(fila);
        }
    }

    private void limpiar() {
        txtID.setText("");
        txtNombre.setText("");
        txtDireccion.setText("");
        txtTelefono.setText("");
        tblDatos.clearSelection();
        btnProcesar.setEnabled(false);
    }

